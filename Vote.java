    private void formAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_formAncestorAdded
        System.out.println("FOCUS");
        // Αλαγή τίτλου για τις εκλογές σύμφωνα με το αρχείο του ψηφοδελτίου
        Title.setText(SecureVote.electionTitle);        
        Title.repaint();
        // Προσθήκη των υποψηφίων στη λίστα του ψηφοδελτίου
        DefaultListModel canditates= new DefaultListModel();
        for (var c: SecureVote.candidate){
            canditates.addElement(c);
        }
        CanditateList.setModel(canditates);
        CanditateList.setVisible(false);
        CanditateList.repaint();
        VoteNow.setEnabled(false);
        PostVote.setEnabled(false);
        EndVoting.setEnabled(false);
        this.repaint();
    }//GEN-LAST:event_formAncestorAdded

    private void StartVotingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartVotingActionPerformed
        JPasswordField pwd = new JPasswordField();
        int action = JOptionPane.showConfirmDialog(this, pwd,"Enter Password",JOptionPane.OK_CANCEL_OPTION);
        if(action != 0){
            JOptionPane.showMessageDialog(this,"Ακύρωση έναρξης ψηφοφορίας");
            
        }
        else{
            //JOptionPane.showMessageDialog(null,"Your password is "+new String(pwd.getPassword()));
            String pass =new String( pwd.getPassword());
            //JOptionPane.showMessageDialog(null,"Your password is "+new String(pwd.getPassword()));
            
            if (SecureVote.authenticate("admin", pass)){
                JOptionPane.showMessageDialog(this, "Έναρξη ψηφοφορίας!");
                StartVoting.setEnabled(false);
                EndVoting.setEnabled(true);
                VoteNow.setEnabled(true);
                
            }else{
                JOptionPane.showMessageDialog(this, "Λάθος κωδικό! Η Ψηφοφορία δεν μπορεί να αρχίσει!");
            }
        }
        //System.exit(0);
    }//GEN-LAST:event_StartVotingActionPerformed

