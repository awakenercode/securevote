    private void VoteNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VoteNowActionPerformed
        String user = username.getText();
        String pass = new String (Password.getPassword());
        //System.out.println(user+" - "+pass);
        if (SecureVote.authenticate(user, pass)){
            if(SecureVote.canVote(user)){
                CanditateList.setVisible(true);
                CanditateList.setEnabled(true);
                int[] sel = {};
                CanditateList.setSelectedIndices(sel);
                VoteNow.setEnabled(false);
                PostVote.setEnabled(true);
                username.setEnabled(false);
                Password.setEnabled(false);
            } else{
                JOptionPane.showMessageDialog(this,"Έχετε ήδη ψηφίσει!");
            }
        }else{
             JOptionPane.showMessageDialog(this, "Λάθος στοιχεία. Δεν μπορείτε να ψηφίσετε!");
        }
    }//GEN-LAST:event_VoteNowActionPerformed

    private void PostVoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PostVoteActionPerformed
        int[] sel = CanditateList.getSelectedIndices();
        if (sel.length>SecureVote.maxVotes){
            JOptionPane.showMessageDialog(this,"Εχετε υπερβεί το μέχιστο όριο επιλογών!.");
            return;
        }else{
            SecureVote.recieveVote(username.getText(), sel);
            CanditateList.setVisible(false);
            CanditateList.setEnabled(false);
            VoteNow.setEnabled(true);
            PostVote.setEnabled(false);
            username.setEnabled(true);
            Password.setEnabled(true);
        }
    }//GEN-LAST:event_PostVoteActionPerformed


